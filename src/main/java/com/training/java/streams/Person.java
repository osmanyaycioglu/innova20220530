package com.training.java.streams;

public class Person
{
    private String name;
    private Integer height;

    public String getName() {
        return name;
    }

    public Person setName(String name) {
        this.name = name;
        return this;
    }

    public Integer getHeight() {
        return height;
    }

    public Person setHeight(Integer height) {
        this.height = height;
        return this;
    }
}
